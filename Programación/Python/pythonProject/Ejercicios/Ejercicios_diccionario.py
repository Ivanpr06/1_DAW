    lista = []
    # Usamos un bucle for para que vaya leyendo canción a canción
    for cancion in range(len(lista_canciones)):
        # Quitamos los dos puntos
        duracion = lista_canciones[cancion]["duracion"].split(":")
        # Usamos este cálculo para calcular los segundos de cada canción
        total = int(duracion[0]) * 60 + int(duracion[1])
